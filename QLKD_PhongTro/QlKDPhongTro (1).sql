CREATE DATABASE QLKDPhongTro;

USE QLKDPhongTro;

CREATE TABLE tblDangNhap (
    Acc INT IDENTITY(1,1) PRIMARY KEY,
    TenDangNhap VARCHAR(50),
    MatKhau VARCHAR(50),
    Email VARCHAR(100)
);

GO
CREATE TABLE tblPhongTro (
    MaPhongTro NVARCHAR(50) PRIMARY KEY,
    ToaNha NVARCHAR(50),
    Tang INT,
    SoPhong INT,
    GiaPhong INT,
    MoTa NVARCHAR(MAX)
);

GO
CREATE TABLE tblKhachThue (
    MaKhachThue INT IDENTITY(1,1) PRIMARY KEY,  
    MaPhongTro NVARCHAR(50),
    HoTen NVARCHAR(100),
    GioiTinh NVARCHAR(10),
    NgaySinh DATE,
    QueQuan NVARCHAR(100),
    SoDienThoai NVARCHAR(15),
    CCCD NVARCHAR(25),
    FOREIGN KEY (MaPhongTro) REFERENCES tblPhongTro(MaPhongTro) ON DELETE SET NULL
);

GO
CREATE TABLE tblHopDong (
    MaPhongTro NVARCHAR(50),
    MaHopDong NVARCHAR(100) PRIMARY KEY,
    MaKhachThue INT, 
    HoTenNguoiKy NVARCHAR(100),  
    TienCoc INT,  
    NgayKy DATE,  
    NgayHetHan DATE, 
    GhiChu NVARCHAR(MAX), 
    FOREIGN KEY (MaPhongTro) REFERENCES tblPhongTro(MaPhongTro) ON DELETE CASCADE,
    FOREIGN KEY (MaKhachThue) REFERENCES tblKhachThue(MaKhachThue) ON DELETE CASCADE
);

GO
CREATE TABLE tblDienNuoc (
    MaDienNuoc INT IDENTITY(1,1) PRIMARY KEY,       
    MaPhongTro NVARCHAR(50),                       
    ThangNam DATE,                               
    SoDienCu INT,                                 
    SoDienMoi INT,
    TieuThuDien AS (SoDienMoi - SoDienCu),         
    TienDien AS ((SoDienMoi - SoDienCu) * DonGiaDien),  
    SoNuocCu INT,                                 
    SoNuocMoi INT,
    TieuThuNuoc AS (SoNuocMoi - SoNuocCu),        
    TienNuoc AS ((SoNuocMoi - SoNuocCu) * DonGiaNuoc),  
    DonGiaDien INT,                               
    DonGiaNuoc INT,									
	FOREIGN KEY (MaPhongTro)  REFERENCES tblPhongTro(MaPhongTro) ON DELETE CASCADE                         
);
GO
CREATE TABLE tblHoaDon (
    MaHoaDon INT IDENTITY(1,1) PRIMARY KEY, 
    MaPhongTro NVARCHAR(50),              
    ThangNam DATE NOT NULL,           
    TienVeSinh INT NOT NULL DEFAULT 0,   
    Internet INT NOT NULL DEFAULT 0,  
	DichVuKhac int not null default 0,
    KhuyenMai INT NOT NULL DEFAULT 0,
	GhiChu nvarchar(max),
    TrangThai NVARCHAR(20) 
    FOREIGN KEY (MaPhongTro) REFERENCES tblPhongTro(MaPhongTro) ON DELETE CASCADE                 
);
GO
CREATE TABLE tblChiTietHoaDon (
    STT INT IDENTITY(1,1) PRIMARY KEY,
    MaHoaDonCT INT,
    TenDichVu NVARCHAR(50),
    ChiSoCu INT,
    ChiSoMoi INT,
    SoLuong INT,
    DonGia INT,
    ThanhTien INT,
	FOREIGN KEY (MaHoaDonCT) REFERENCES tblHoaDon(MaHoaDon) ON DELETE CASCADE
);
GO
